    <script>
        function programmingList() {
            document.getElementById("myDropdown1").classList.toggle("show");
        }

        function dabi() {
            hideAllTeams();
            var teamTekst = document.querySelector(".dabi");
            teamTekst.style.display = "flex";
        }

        function kanaalregie() {
            hideAllTeams();
            var teamTekst = document.querySelector(".kanaalregie");
            teamTekst.style.display = "flex";
        }

        function klantsignaal() {
            hideAllTeams();
            var teamTekst = document.querySelector(".klantsignaal");
            teamTekst.style.display = "flex";
        }

        function ketenregie() {
            hideAllTeams();
            var teamTekst = document.querySelector(".ketenregie");
            teamTekst.style.display = "flex";
        }

        function hideAllTeams() {
            var teams = document.querySelectorAll(".team-text");
            teams.forEach(function(team) {
                team.style.display = "none";
            });
        }


        window.onclick = function(event) {
            if (!event.target.matches('.dropbtn')) {
                var dropdowns = document.getElementsByClassName("dropdown-content");
                var i;
                for (i = 0; i < dropdowns.length; i++) {
                    var openDropdown = dropdowns[i];
                    if (openDropdown.classList.contains('show')) {
                        openDropdown.classList.remove('show');
                    }
                }
            }
        }
    </script>


